export function getDayContent(dayNumber: number): MeditationDay | undefined {
  return program.find((day) => day.dayNumber === dayNumber);
}

export function getWeekDays(week: number): MeditationDay[] {
  return program.filter((day) => day.week === week);
}

export function getWeekLabel(week: number): string {
  switch (week) {
    case 1:
      return 'Week 1: Foundation';
    case 2:
      return 'Week 2: Exploration';
    case 3:
      return 'Week 3: Deepening';
    case 4:
      return 'Week 4: Integration';
    case 5:
      return 'Days 29-30: Celebration';
    default:
      return `Week ${week}`;
  }
}
