// ---------- Helper Functions ----------

export function getRandomQuote(category: keyof typeof marcusQuotes): string {
  const quotes = marcusQuotes[category];
  const index = Math.floor(Math.random() * quotes.length);
  return quotes[index];
}

export function getGreeting(): string {
  const hour = new Date().getHours();

  if (hour >= 5 && hour < 12) {
    return getRandomQuote("greeting_morning");
  } else if (hour >= 12 && hour < 17) {
    return getRandomQuote("greeting_afternoon");
  } else {
    return getRandomQuote("greeting_evening");
  }
}

export function getPhaseIntro(
  phase: "excavation" | "clarity" | "vision" | "decision"
): string {
  const categoryMap = {
    excavation: "excavation_intro",
    clarity: "clarity_intro",
    vision: "vision_intro",
    decision: "decision_intro",
  } as const;

  return getRandomQuote(categoryMap[phase]);
}

export function getNudge(): string {
  const nudgeCategories: (keyof typeof marcusQuotes)[] = [
    "go_deeper",
    "stay_with_it",
    "challenge",
  ];
  const category =
    nudgeCategories[Math.floor(Math.random() * nudgeCategories.length)];
  return getRandomQuote(category);
}
